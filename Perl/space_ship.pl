# ********************************************************************************
# スペースシップ演算子
# ********************************************************************************

use 5.016;
use Data::Dumper;

my @list = sort(1, 33, 4, 15, 2);

# Perlのソートは文字列として扱ってしまう
say Dumper @list;

# スペース演算子を使うことで、数値としてソートできる
@list = sort {$a <=> $b} (1, 33, 4, 15, 2);
say Dumper @list;

# $bと$aを逆にすることで、逆順ソートできる
@list = sort {$b <=> $a} (1, 33, 4, 15, 2);
say Dumper @list;
